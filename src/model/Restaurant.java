package model;

public class Restaurant {
    String nume;
    int nrStele;
    String oras;
    String strada;
    String nrTelefon;

    public Restaurant(String nume, int nrStele, String oras, String strada, String nrTelefon) {
        this.nume = nume;
        this.nrStele = nrStele;
        this.oras = oras;
        this.strada = strada;
        this.nrTelefon = nrTelefon;
    }
}

